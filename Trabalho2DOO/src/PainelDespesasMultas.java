    private void jB_editarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jB_editarMouseClicked
        //obter qual a celula foi selecionada
        int selectedRow = jTable_historico.getSelectedRow();

        if (selectedRow >= 0) {
            // Aparecer o painel
            jPanel1.setVisible(true);
            jPanel2.setVisible(false);

            // Obter os valores dos produtos que serão editados
            int contadorLinha = 0; // Contador para rastrear o índice da linha atual

            for (DespesasMultasClasse despesa : principalJFrame.usuario.getDespesas()) {
                if (contadorLinha == selectedRow) {
                    jTF_valor.setText(despesa.getValor());
                    jFTF_data.setText(despesa.getData());
                    jCB_tipo.setSelectedItem(despesa.getTipo());

                    // Remover
                    ((DefaultTableModel) jTable_historico.getModel()).removeRow(selectedRow);
                    System.out.println("Despesa removida!");

                    // Remover a despesa do ArrayList
                    principalJFrame.usuario.getDespesas().remove(despesa);
                    break;
                }

                contadorLinha++; // adiciona no contador de linha
            }
        } else {
            System.out.println("Nenhuma célula selecionada.");
            JOptionPane.showMessageDialog(null, "Nenhuma célula selecionada.");
            
        }
        
        
    }//GEN-LAST:event_jB_editarMouseClicked

