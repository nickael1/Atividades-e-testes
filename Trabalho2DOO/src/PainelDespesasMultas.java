    public void deserealizar(){
       try {
            
            FileInputStream arquivo = new FileInputStream(nomeDoArquivo);
            ObjectInputStream in = new ObjectInputStream(arquivo);
            
            
            objetoRecuperado = (DespesasMultasClasse)in.readObject();
            in.close();
            arquivo.close();
            
            System.out.println("desserializado com sucesso!");
        } catch (IOException e) {
            System.out.println("Erro de leitura do arquivo ou falha de entrada/saída.");
        }
        catch(ClassNotFoundException e){
            System.out.println("Classe não encontrada!");
        } 
    }
    
    
    private void jB_salvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jB_salvarMouseClicked
        //Empacotamento.gravarArquivoBinario(despesasArray, "Despesas.ser");
        if (jTF_valor.getText().equals("") || jFTF_data.getText().equals("") || jCB_tipo.getSelectedIndex() == 0){
            //Som de erro!
                AudioErro audioErro = new AudioErro();
                audioErro.AudioErro();
            JOptionPane.showMessageDialog(null, "Você deve preencher todos os campos!", "Atenção", JOptionPane.WARNING_MESSAGE);
        }else {
            //Adicionar
            DespesasMultasClasse despesas = new DespesasMultasClasse((String) jCB_tipo.getSelectedItem(), jTF_valor.getText(), jFTF_data.getText());
            principalJFrame.usuario.getDespesas().add(despesas);
            //despesasArray.add(despesas);
            System.out.println(jCB_tipo.getSelectedItem());
            //
            //Limpar campos
            jTF_valor.setText("");
            jFTF_data.setText("");
            jCB_tipo.setSelectedIndex(0);
            preencherTabela();
            JOptionPane.showMessageDialog(null, "Os dados inseridos foram salvos!");
            
            try{//Serrealização
                FileOutputStream arquivo = new FileOutputStream(nomeDoArquivo);
                ObjectOutputStream out = new ObjectOutputStream(arquivo);

                out.writeObject(despesas);
                out.close();
                arquivo.close();
                System.out.println("foi serealizado!");
            } catch (IOException e) {
                System.out.println("ERRO na serealização");
            }
        }
    }//GEN-LAST:event_jB_salvarMouseClicked
    
    DefaultTableModel modeloTabela = new DefaultTableModel();
    public void preencherTabela(){
        
        int n = principalJFrame.usuario.getDespesas().size();
        System.out.println(n);
        String colunas[] = {"Tipo", "Valor", "Data"};
        String linhas[][] = new String[n][3];
        int j = 0;
        
        for (int i = 0; i < n; i++) {
            linhas[i][j] = principalJFrame.usuario.getDespesas().get(i).getTipo();
            j++;
            linhas[i][j] = principalJFrame.usuario.getDespesas().get(i).getValor();
            j++;
            linhas[i][j] = principalJFrame.usuario.getDespesas().get(i).getData();
            j=0;
        }
        this.modeloTabela = new DefaultTableModel(linhas, colunas){
            public boolean isCellEditable(int rowIndex, int mColIndex){
                return false;
            }
        };
        jTable_historico.setModel(modeloTabela);       
    }
    
